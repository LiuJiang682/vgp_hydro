CREATE OR REPLACE TABLE SAMP_METADATA (
  id         BIGINT NOT NULL PRIMARY KEY,
  LOADER_ID BIGINT NOT NULL, 
  SITE_ID BIGINT NOT NULL,
  SAMPLE_ID BIGINT NOT NULL,
  FILE_NAME VARCHAR(50) DEFAULT NULL NOT NULL,
  ROW_NUMBER VARCHAR(10) NOT NULL, 
  CORE_ID BIGINT,
  LAB_CODE VARCHAR(25),
  "TYPE" VARCHAR(100),
  PREP_CODE VARCHAR(100),
  SAMP_DATE TIMESTAMP,
  IGSN VARCHAR(25) NOT NULL,
  SAMP_TOP DECIMAL NOT NULL,
  SAMP_BOTTOM DECIMAL NOT NULL,
  STAND_WATER_LVL DECIMAL NOT NULL,
  PUMPING_DEPTH DECIMAL NOT NULL,
  REFERENCE VARCHAR(100) NOT NULL,
  SAMP_AREA_DESC VARCHAR(1000),
);